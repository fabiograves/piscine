void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int swap;

	i = 0;
	j = 0;
	while (j < size - 1)
	{
		while (i < size - j - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				swap = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = swap;
			}
			i++;
		}
		i = 0;
		j++;
	}
}
